'''
Given inorder and postorder traversal of a tree, construct the binary tree.

Note:
You may assume that duplicates do not exist in the tree.

For example, given

inorder = [9,3,15,20,7]
postorder = [9,15,7,20,3]
Return the following binary tree:

    3
   / \
  9  20
    /  \
   15   7
'''
# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def buildTree(self, inorder, postorder):
        """
        :type inorder: List[int]
        :type postorder: List[int]
        :rtype: TreeNode
        """
        if not inorder:
            return None
        curtree=TreeNode(postorder[-1])
        for k in range(len(inorder)):
            if inorder[k]==curtree.val:
                break
        curtree.left=self.buildTree(inorder[:k], postorder[:k])
        curtree.right=self.buildTree(inorder[k+1:], postorder[k:-1])
        return curtree
